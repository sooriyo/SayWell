                .font(.system(.footnote, design: .rounded))
                .foregroundStyle(SayWellTheme.ink.opacity(0.68))
                .fixedSize(horizontal: false, vertical: true)
        }
    }
}

struct TranslationSourceBadge: View {
    let source: TranslationSource

    var body: some View {
        Text(label)
            .font(.system(.caption2, design: .rounded).weight(.semibold))
            .padding(.horizontal, 8)
            .padding(.vertical, 4)
            .background(tint.opacity(0.12), in: Capsule())
            .foregroundStyle(tint)
            .accessibilityLabel(accessibilityLabel)
    }

    private var label: String {
        switch source {
        case .builtin, .cache: "Instant"
        case .model: "AI"
        }
    }

    private var tint: Color {
        switch source {
        case .builtin, .cache: SayWellTheme.brand
        case .model: .orange
        }
    }

    private var accessibilityLabel: String {
        switch source {
        case .builtin: "Built-in phrase"
        case .cache: "Served from cache"
        case .model: "Generated by AI"
        }
    }
}
