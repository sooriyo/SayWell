import SwiftUI

// MARK: - Loading indicator

struct AnimatedDotsView: View {
    var color: Color = SayWellTheme.lagoon.opacity(0.85)
    var dotSize: CGFloat = 5

    var body: some View {
        TimelineView(.periodic(from: .now, by: 0.32)) { context in
            HStack(spacing: 4) {
                ForEach(0..<3, id: \.self) { index in
                    Circle()
                        .fill(color.opacity(opacity(for: index, at: context.date)))
                        .frame(width: dotSize, height: dotSize)
                }
            }
        }
        .accessibilityLabel("Loading")
    }

    private func opacity(for index: Int, at date: Date) -> Double {
        let phase = date.timeIntervalSinceReferenceDate / 0.32
        let wave = sin((phase + Double(index) * 0.34) * .pi * 2)
        return 0.32 + 0.68 * max(0, wave)
    }
}

// MARK: - Result area states

struct TranslationLoadingCard: View {
    let phrase: String

    var body: some View {
        VStack(alignment: .leading, spacing: 14) {
            HStack(spacing: 10) {
                AnimatedDotsView()
                Text(loadingMessage)
                    .font(.system(.subheadline, design: .rounded).weight(.semibold))
                    .foregroundStyle(SayWellTheme.lagoon.opacity(0.9))
            }

            if !phrase.isEmpty {
                Text("“\(phrase)”")
                    .font(.system(.title3, design: .serif))
                    .foregroundStyle(SayWellTheme.ink.opacity(0.62))
                    .frame(maxWidth: .infinity, alignment: .leading)
                    .lineLimit(3)
            }
        }
        .padding(18)
        .background(SayWellTheme.foam.opacity(0.92), in: RoundedRectangle(cornerRadius: 18, style: .continuous))
        .overlay {
            RoundedRectangle(cornerRadius: 18, style: .continuous)
                .strokeBorder(SayWellTheme.brand.opacity(0.2), lineWidth: 1)
        }
        .accessibilityElement(children: .combine)
        .accessibilityLabel("Translating \(phrase)")
    }

    private var loadingMessage: String {
        let trimmed = phrase.trimmingCharacters(in: .whitespacesAndNewlines)
        guard trimmed.count > 22 else { return "Finding the right English" }
        return "Translating your Singlish"
    }
}

